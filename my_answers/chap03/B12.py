"""
項目 | 説明
なぜ100か？ | x=100のとき f(100) は100万以上になるので、普通の範囲のNなら十分だから
問題設定は？ | 超巨大なNは出ないと暗黙に仮定している
もし超巨大なNなら？ | Rightをもっと大きく（たとえばNそのものまで）広げる必要がある

項目 | 説明
二分探索の特徴 | 1回ごとに探索範囲が1/2になる
最初の幅 | 100.0
20回後の幅 | 約 0.00010.00010.0001 になる
許容される誤差 | 0.0010.0010.001 以下
結論 | 20回で十分に誤差条件を満たせる

初期幅: 100
↓ (1回半分)
幅: 50
↓ (2回半分)
幅: 25
↓
...
↓ (20回後)
幅: 0.000095367

"""

def f(x):
    return x*x*x + x

N = int(input())

# 二分探索
Left = 0.0
Right = 100.0

for i in range(20):
    Mid = (Left+Right) / 2
    val = f(Mid)

    if val > N:
        Right = Mid
    else:
        Left = Mid

print(Mid)